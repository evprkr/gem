class Keybinds:
    def __init__(self):
        self.Escape = 27
        self.Enter = 13
        self.CursorLeft = ord('h')
        self.CursorRight = ord('l')
        self.CursorUp = ord('k')
        self.CursorDown = ord('j')
        self.Insert = ord('i')
        self.Exit = ord('q')

Keys = Keybinds()
